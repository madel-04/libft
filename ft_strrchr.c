#include "libft.h"

char	*strrchr(const char *s, int c)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (s[i] != '\0')
	{
		if (s[i] == c)
			j = i;
		i++;
	}
	if (c == '\0')
		return (*s[i]);
	if (j = 0)
		return NULL;
	else
		return (*s[j]);
}
